package com.mycompany.route.data;


public class Proizvod {

    private String ime;
    private String opis;
    private double cena;

    public Proizvod() {
    }

    public Proizvod(String ime, String opis, double cena) {
        this.ime = ime;
        this.opis = opis;
        this.cena = cena;
    }

    public String getIme() {
        return ime;
    }

    public void setIme(String ime) {
        this.ime = ime;
    }

    public String getOpis() {
        return opis;
    }

    public void setOpis(String opis) {
        this.opis = opis;
    }

    public double getCena() {
        return cena;
    }

    public void setCena(double cena) {
        this.cena = cena;
    }

}
